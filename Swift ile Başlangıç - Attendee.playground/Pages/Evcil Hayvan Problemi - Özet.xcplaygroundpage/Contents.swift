//: ## Evcil Hayvan Problemi - Özet
//: Bütün güncellemeleri yaptıktan sonra, kodlarınız şöyle görünüyor olmalı:
// Köpeklerin Sayısı
6

// Kedilerin Sayısı
5

// Kaplumbağaların Sayısı
2

// Hamster'ların Sayısı
1

// Hayvanların Toplamı
6 + 5 + 2 + 1

// Memelilerin Toplamı
6 + 5 + 1
//: Playground sayfalarını bu şekilde kullanmak arkadaşınızın sorununu çözdü. Her türden kaç hayvan olduğunu, tüm hayvanların sayısını ve tüm memelilerin sayısını takip ettiniz.
//:
//: Ancak, deneyi yaptıkça hata yapmanın çok kolay olduğunu fark etmiş olabilirsiniz. Her sayıyı birkaç yerde değiştirmeniz gerekti. Eğer sayıları her yerde güncellemeyi unutsaydınız, toplamlarınız yanlış olurdu.
//:
//: Ayrıca hangi sayı ile hangi hayvan türünü takip ettiğinizi unutmamanız gerekti. Her sayıyı ayıran açıklamalar olmadan, her şeyi bir arada tutmak oldukça zor olacaktır.
//:
//: Kod yazarken işinize en çok yarayacak şeylerden biri, bir şeylere isim verebilmeniz olabilir. Aynı şeyi ifade eden sayıları tekrar yazmak yerine, onlara bir isim verebilirsiniz - ve ihtiyacınız olduğunda, kodunuzda herhangi bir yerde o sayılar yerine verdiğiniz isimleri kullanabilirsiniz! 😉
