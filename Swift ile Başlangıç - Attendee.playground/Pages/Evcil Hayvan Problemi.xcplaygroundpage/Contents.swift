//: ## Evcil Hayvan Problemi 🐶
//: Arkadaşınızın bir evcil hayvan gösterisi düzenleyeceğini ve gösteride her hayvandan kaç tane olacağını takip etmesi gerektiğini hayal edin.
//: Aynı zamanda arkadaşınız, gösteride toplam kaç hayvan ve memeli hayvan olacağını da takip etmeli.
//:
//: Şimdi yanınızda sadece Xcode yüklü bir dizüstü bilgisayar olduğunu düşünün. Belki de arkadaşınıza yardım etmek için bir Playground sayfası kullanabilirsiniz!
//:
//: Şu ana kadar öğrendiklerinizi kullanarak, her hayvanın sayısını bir açıklama ve bir sayı ile takip edebilirsiniz:
// Köpeklerin Sayısı
5

// Kedilerin Sayısı
4

// Kaplumbağaların Sayısı
3
//: Sonrasında bu sayıları yeni kod satırlarına kopyalayabilir ve Playground ortamının sizin için hesaplama yapmasını sağlayabilirsiniz:
// Hayvanların Toplamı
5 + 4 + 3

// Memelilerin Toplamı
5 + 4
//: - callout(Deney): Hafta ilerledikçe, gösteride hangi hayvanların olacağı ile ilgili daha fazla bilgi edindiniz.\
//:Toplamları güncellemek için yukarıdaki kodu değiştirin.
//:
//: Örneğin, eğer birisi yanında bir köpek getiriyorsa, `Köpeklerin Sayısı`nı `5`'ten `6`'ya değiştirin. `5` sayısını kullandığımız diğer her yerde de (`Hayvanların Toplamı` ve `Memelilerin Toplamı`) `6` sayısı ile değiştirin.
//:
//: Eğer birisi yanında yeni bir hayvan getirecekse, aynı diğer hayvan sayılarını yazdığımız şekilde onu da ekleyin. Yeni bir satırda yeni bir açıklama (`// Hamster'ların Sayısı` gibi) ve sonraki satıra da bir sayı yazın. Toplamları güncellemeyi unutmayın.
//:
//: - Başka birisi de bir köpek getiriyor.
//: - Kaplumbağalardan birisi hasta ve gelemeyecek. 😥
//: - Bir kişi daha bir kedi getiriyor.
//: - Birisi de bir hamster getirmeye karar verdi (bir memeli).
//:
//:  Bu problemi çözmenin daha iyi bir yolu olabilir mi? 🤔 Bunun üzerine birkaç dakika düşünelim ve sonra ilerleyelim.
//:
