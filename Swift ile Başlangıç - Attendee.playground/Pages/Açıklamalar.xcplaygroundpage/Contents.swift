//: ## Yorum Satırları
//: Programcılar, kodlarının tam üzerinde açıklayıcı küçük notlar bırakırlar.
//: Bu, insanlar kodu daha sonra okuduklarında anlamalarına yardımcı olur.
//: Bu notlara _yorum satırı (İngilizce: comment)_ ismi verilir.
//:
//: Açıklamalar, Playground ortamı tarafından göz ardı edilir ve kodunuzun çalışmasını etkilemez.
//: 
//: Açıklamalar çift eğik çizgi işareti `//` ile başlar :
// Bu bir açıklamadır, kodun çalışmasını etkilemez.
34 + 56 + 230
//: Açıklamalar göz ardı edildikleri için, açıklama olarak işaretlenmiş kod satırları için sonuç alanında bir sonuç gösterilmez.
// 200 + 34 + 45
//: - Callout(Deney):
//:     - Başına iki slash işareti ekleyerek `34 + 56 + 230` işlemini _açıklamaya çevirmeyi_ deneyin.
//:     - `200 + 34 + 45` işlemini başındaki çift slash işaretini silerek _açıklamadan çıkarmayı_ deneyin.
//:     - Mevcut işlemlerin bazılarını düzenlemeyi ve birkaç tane de kendiniz eklemeyi deneyin.\
//: Her değişiklik yaptığınızda sonuç alanının güncellendiğini görebilirsiniz.
//:
//: Derin bir nefes alın ve bir sonraki sayfaya geçin 🙂
